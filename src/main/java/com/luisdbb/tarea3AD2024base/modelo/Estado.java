package com.luisdbb.tarea3AD2024base.modelo;

/**
 * Enumeración que representa los posibles estados de una FE.
 * 
 * PENDIENTE: la formación aún no ha comenzado.
 * EN_CURSO: la formación se está realizando actualmente.
 * FINALIZADO: la formación ha terminado.
 */
public enum Estado {
    PENDIENTE, EN_CURSO, FINALIZADO
}