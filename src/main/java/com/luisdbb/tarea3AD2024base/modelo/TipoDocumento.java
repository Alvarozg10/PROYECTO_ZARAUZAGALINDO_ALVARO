package com.luisdbb.tarea3AD2024base.modelo;

/**
 * Enumeración que define los distintos tipos de documentos
 * que pueden estar asociados a una FCT.
 * 
 * CONTRATO: documento contractual entre el estudiante y la empresa.
 * INFORME: documento de seguimiento o evaluación.
 * MEMORIA: documento final del estudiante.
 * OTRO: cualquier otro tipo de documento no especificado.
 */
public enum TipoDocumento {
    CONTRATO,
    INFORME,
    MEMORIA,
    OTRO
}